#include <bits/stdc++.h>

#include "../../library/lib.hpp"


class AIchihimeAndTriangle {
public:
    void solve(istream &in, ostream &out) {
        int T;
        in >> T;

        while (T--) {
            long a, b, c, d;
            in >> a >> b >> c >> d;
            out << a << " " << c << " " << c << endl;
        }

    }
};
